zita=0.7;
wn=4;
t=[0:0.1:2];
k1=wn^2;
k2=(2*zita*wn-2)/(wn^2);
numg=[0 0 k1];
deng=[0 1 2];
numh=[0 0 k2];
denh=[0 0 1];
[numr, denr]=feedback(numg, deng, numh, denh,-1);
numg=[0 0 1];
deng=[0 1 0];
[nums, dens]=series(numr, denr, numg, deng);
[numr1, denr1]=cloop(nums, dens, -1);
subplot(3,1,1);
step(numr1, denr1,t);
subplot(3,1,2);
impulse(numr1, denr1,t);
subplot(3,1,3);
numr1=[0 numr1];
denr1=[denr1 0];
step(numr1,denr1,t);